// Seeded database of common exercises with muscle groups and FP type mappings.
// This is the source of truth for all exercises in the app.

import type { StatType } from '@/types';

// -----------------------------------------------------------------------------
// Muscle Group Types
// -----------------------------------------------------------------------------

export type MuscleGroup =
  | 'chest'
  | 'back'
  | 'shoulders'
  | 'biceps'
  | 'triceps'
  | 'forearms'
  | 'quads'
  | 'hamstrings'
  | 'glutes'
  | 'calves'
  | 'core'
  | 'traps';

export type MovementPattern =
  | 'push_horizontal'
  | 'push_vertical'
  | 'pull_horizontal'
  | 'pull_vertical'
  | 'hinge'
  | 'squat'
  | 'lunge'
  | 'core'
  | 'isolation';

export type Equipment =
  | 'barbell'
  | 'dumbbell'
  | 'cable'
  | 'machine'
  | 'bodyweight'
  | 'kettlebell'
  | 'band';

// -----------------------------------------------------------------------------
// Exercise Definition
// -----------------------------------------------------------------------------

export interface ExerciseDefinition {
  id: string;
  name: string;
  muscleGroups: MuscleGroup[];
  primaryMuscle: MuscleGroup;
  movementPattern: MovementPattern;
  equipment: Equipment[];
  defaultSets: number;
  defaultReps: string; // e.g., "8-12", "5x5", "10-15"
  defaultRestSeconds: number;
  isCompound: boolean;
  fpTypes: StatType[]; // Which FP types this exercise generates
}

